package com.model;

public class Provincia {
	private int cod_provincia;
	private String denominazione;
	private String sigla_autom;
	public int getCod_provincia() {
		return cod_provincia;
	}
	public void setCod_provincia(int cod_provincia) {
		this.cod_provincia = cod_provincia;
	}
	public String getDenominazione() {
		return denominazione;
	}
	public void setDenominazione(String denominazione) {
		this.denominazione = denominazione;
	}
	public String getSigla_autom() {
		return sigla_autom;
	}
	public void setSigla_autom(String sigla_autom) {
		this.sigla_autom = sigla_autom;
	}
	
	

}
